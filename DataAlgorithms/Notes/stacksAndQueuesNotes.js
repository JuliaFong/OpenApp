// WHAT IS A STACK

// Stacks are a Last In First Out (LIFO) data structure
// the last node added to a stack is always the first node to be removed,
//  and as a result, the first node added 
// is always the last node removed

// We add this to the top of a stack
// We remove things from the top of the stack

// We can use JS arrays to implement a basi stack

// Array#push => Add to the top of the stack
// Arrayp#pop => will remove from the top of the stack

